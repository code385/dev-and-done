// Get all demos as a flat array
export const allDemos = Object.values(demoCategories).flatMap(category => 
  category.demos.map(demo => ({
    ...demo,
    category: category.name,
    categoryId: Object.keys(demoCategories).find(key => demoCategories[key] === category)
  }))
);

// Get demo by ID
export const getDemoById = (id) => {
  return allDemos.find(demo => demo.id === id);
};

// Get demos by category
export const getDemosByCategory = (categoryId) => {
  return demoCategories[categoryId]?.demos || [];
};

